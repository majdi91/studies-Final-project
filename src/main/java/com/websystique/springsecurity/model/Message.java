package com.websystique.springsecurity.model;

public class Message {
	
	private String txt;
	private String sender;
	public String getTxt() {
		return txt;
	}
	public void setTxt(String txt) {
		this.txt = txt;
	}
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	

}
